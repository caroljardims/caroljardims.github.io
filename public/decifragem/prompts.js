/** Sorteia categoria (objeto) e um prompt (string) evitando repetição opcional */
export function sortearPrompt(excluir = new Set()) {
  const cats = [...CATEGORIAS];
  for (let attempt = 0; attempt < 50; attempt++) {
    const cat = cats[Math.floor(Math.random() * cats.length)];
    const pool = cat.prompts.filter((p) => !excluir.has(`${cat.id}:${p}`));
    if (pool.length === 0) continue;
    const prompt = pool[Math.floor(Math.random() * pool.length)];
    return { categoria: cat.nome, categoriaId: cat.id, emoji: cat.emoji, prompt };
  }
  const cat = CATEGORIAS[Math.floor(Math.random() * CATEGORIAS.length)];
  const prompt = cat.prompts[Math.floor(Math.random() * cat.prompts.length)];
  return { categoria: cat.nome, categoriaId: cat.id, emoji: cat.emoji, prompt };
}
